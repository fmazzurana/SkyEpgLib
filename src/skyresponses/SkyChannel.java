package skyresponses;

public class SkyChannel {
	
	private int id;
	private String name;
	private int number;
	private int service;
	private String channellogonew;
	private String channellogo;
	private String channelvisore;
	private String logomsite;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	
	public int getService() {
		return service;
	}
	public void setService(int service) {
		this.service = service;
	}
	
	public String getChannellogonew() {
		return channellogonew;
	}
	public void setChannellogonew(String channellogonew) {
		this.channellogonew = channellogonew;
	}
	
	public String getChannellogo() {
		return channellogo;
	}
	public void setChannellogo(String channellogo) {
		this.channellogo = channellogo;
	}
	
	public String getChannelvisore() {
		return channelvisore;
	}
	public void setChannelvisore(String channelvisore) {
		this.channelvisore = channelvisore;
	}
	
	public String getLogomsite() {
		return logomsite;
	}
	public void setLogomsite(String logomsite) {
		this.logomsite = logomsite;
	}
}
